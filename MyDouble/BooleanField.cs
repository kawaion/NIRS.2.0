using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace MyDouble
{
    static class BooleanField
    {
        public static bool Add(bool Value1, bool Value2)
        {
            return Value1 ^ Value2;
        }
        public static bool Subtract(bool Value1, bool Value2)
        {
            return Value1 ^ Value2;
        }
    }
}
